package edu.cmu.xuzheng.project4android;

import android.graphics.Bitmap;

/**
 * @author Xu Zheng
 * Andrew ID: xuzheng
 * <p>
 * This class is the POJO for the IP information
 */
public class IPInfoResult {
    private String continent;
    private String country;
    private String countryCapital;
    private Bitmap countryFlag;
    private String countryFlagUrl;
    private String state;
    private String city;
    private String district;
    private String zipcode;
    private String isp;
    private String organization;
    private String latitude;
    private String longitude;
    private String message;

    public String getContinent() {
        return continent;
    }

    public String getCountryFlagUrl() {
        return countryFlagUrl;
    }

    public void setCountryFlagUrl(String countryFlagUrl) {
        this.countryFlagUrl = countryFlagUrl;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCountryCapital() {
        return countryCapital;
    }

    public void setCountryCapital(String countryCapital) {
        this.countryCapital = countryCapital;
    }

    public Bitmap getCountryFlag() {
        return countryFlag;
    }

    public void setCountryFlag(Bitmap countryFlag) {
        this.countryFlag = countryFlag;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getZipcode() {
        return zipcode;
    }

    public void setZipcode(String zipcode) {
        this.zipcode = zipcode;
    }

    public String getIsp() {
        return isp;
    }

    public void setIsp(String isp) {
        this.isp = isp;
    }

    public String getOrganization() {
        return organization;
    }

    public void setOrganization(String organization) {
        this.organization = organization;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}

